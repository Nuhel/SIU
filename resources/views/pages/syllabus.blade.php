                                                <h5>Course Pattern and Credit Structure</h5>
                                                <p>The undergraduate and graduate programs are covered through a set of  theoretical and laboratory course, fieldwork, design and project/thesis work.
                                                    Please refer to individual department for course listings.</p>
                                                <br><br>
                                                <h5>Assignment of Credits</h5>
                                                <ol>
                                                    <li><strong><i class="fas fa-long-arrow-alt-right"></i> Theoretical Courses : </strong>one lecture per week per semester will be equivalent to one credit Thus a three credit hour course will have three lectures per week through a semester.</li>
                                                    <br>
                                                    <li><strong><i class="fas fa-long-arrow-alt-right"></i> Laboratory/Field/Design/Project/Thesis work :</strong> Credits for laboratory/Field/or design work usually will be half of the theoretical call hours per semester. Credits are also assigned to project/thesis work taken by students. The amount of credits assigned to each of these may vary from discipline to discipline.</li>
                                                </ol>
                                                <br><br>
                                                <h5>Types of courses</h5>
